    private String generateAIDescription(String serviceTitle) {
        try {
            String prompt = "Write a compelling job description for a freelance " + serviceTitle +
                    " service. The description should be clear, specific, and directly highlight" +
                    " the skills, experience, and qualities required. The response should be structured as a professional job posting," +
                    " starting with 'I'm looking for' and limited to 4-5 sentences.";

            JSONObject requestData = new JSONObject();
            requestData.put("inputs", prompt);

            URL url = new URL(API_URL);
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();
            connection.setRequestMethod("POST");
            connection.setRequestProperty("Authorization", "Bearer " + API_KEY);
            connection.setRequestProperty("Content-Type", "application/json");
            connection.setDoOutput(true);

            try (OutputStream os = connection.getOutputStream()) {
                os.write(requestData.toString().getBytes());
                os.flush();
            }

            StringBuilder response = new StringBuilder();
            try (BufferedReader in = new BufferedReader(new InputStreamReader(connection.getInputStream()))) {
                String inputLine;
                while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
                }
            }

            JSONArray responseArray = new JSONArray(response.toString());
            if (responseArray.length() > 0) {
                JSONObject firstResponse = responseArray.getJSONObject(0);
                if (firstResponse.has("generated_text")) {
                    String generatedText = firstResponse.getString("generated_text").trim();

                    // ‚úÖ Ensure response does not repeat the prompt
                    if (generatedText.toLowerCase().contains(prompt.toLowerCase())) {
                        generatedText = generatedText.replace(prompt, "").trim();
                    }

                    // ‚úÖ Ensure response starts correctly
                    generatedText = formatAIResponse(serviceTitle, generatedText);

                    return generatedText; // ‚úÖ Return cleaned description
                }
            }

            return null; // No valid description generated
        } catch (Exception e) {
            e.printStackTrace();
            showAlert("Error", "Failed to generate AI description: " + e.getMessage(), Alert.AlertType.ERROR);
            return null;
        }
    }


    private String formatAIResponse(String serviceTitle, String aiResponse) {
        if (aiResponse == null || aiResponse.isEmpty()) {
            return "I'm looking for a " + serviceTitle + " expert."; // Fallback response
        }

        // Normalize whitespace
        aiResponse = aiResponse.trim();

        // Remove unwanted AI explanations and reasoning (Anything before the first "I'm looking for")
        int startIndex = aiResponse.toLowerCase().indexOf("i'm looking for");
        if (startIndex != -1) {
            aiResponse = aiResponse.substring(startIndex);
        } else {
            // If "I'm looking for" is not found, use a fallback
            aiResponse = "I'm looking for a " + serviceTitle + " expert.";
        }

        // Remove unnecessary asterisks or formatting artifacts
        aiResponse = aiResponse.replaceAll("\\*{2,}", ""); // Removes ** bold formatting
        aiResponse = aiResponse.replaceAll("<.*?>", ""); // Removes any HTML tags if present

        // Ensure the response is properly formatted and trimmed
        aiResponse = aiResponse.trim();

        return aiResponse;
    }



